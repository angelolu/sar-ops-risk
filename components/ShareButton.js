import { Share } from 'react-native';
import IconButton from './IconButton';

const ShareButton = ({ title, content, color }) => {
    const date = new Date();
    const shareEmail = async () => {
        const shareContent = {
            title: title + " from RiskOps",
            message: content + "\nGenerated by the RiskOps app at " + date.toString() + ".",
        };
        const shareOptions = {
            dialogTitle: title,
            subject: title + " from RiskOps",
        };
        try {
            await Share.share(shareContent, shareOptions);
        } catch (error) {
            console.error('Error sharing:', error);
        }
    };

    return (
        <IconButton ionicons_name="share-outline" onPress={shareEmail} color={color} size={24} />
    );
};

export default ShareButton;